    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ProductRequest $request)
    {
        $input = $request->all();
        if($image = $request->file('image'))
        {
            $destination_path = public_path('uploads/products/');
            $profile_image = date('YmdHis') . '.' . $image->getClientOriginalExtension();
            $image->move($destination_path,$profile_image);
            $photo = $profile_image;
        }
        $input['image'] = $photo;
        Product::create($input);
        return redirect()->route('product.index')->withSuccess('Product has been added successfully!');
    }

